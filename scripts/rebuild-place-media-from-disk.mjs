/**
 * Rebuild place-media.generated.ts from files already on disk under public/places/.
 * Useful after partial scrapes or manual downloads.
 *
 *   node scripts/rebuild-place-media-from-disk.mjs
 */
import { readdir, writeFile } from "node:fs/promises";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..");
const PUBLIC_PLACES = join(ROOT, "public", "places");
const OUT_FILE = join(ROOT, "src", "content", "place-media.generated.ts");

const IMAGE_EXT = /\.(jpe?g|png|webp|gif)$/i;

async function main() {
  let dirs = [];
  try {
    dirs = await readdir(PUBLIC_PLACES, { withFileTypes: true });
  } catch {
    console.error("No public/places directory");
    process.exit(1);
  }

  // Preserve videos/menuUrl from previous generated file if present
  let previous = {};
  try {
    const mod = await import("../src/content/place-media.generated.ts");
    previous = mod.placeMediaById ?? {};
  } catch {
    try {
      // fallback: parse nothing
    } catch {
      /* */
    }
  }

  // Read previous via dynamic import won't work for .ts in node easily — read JSON sidecar if any
  // Instead re-read from a simple approach: keep videos from a require of generated by evaluating
  const { readFile } = await import("node:fs/promises");
  let prevRaw = "";
  try {
    prevRaw = await readFile(OUT_FILE, "utf8");
  } catch {
    /* */
  }

  const mediaById = {};

  for (const dirent of dirs) {
    if (!dirent.isDirectory()) continue;
    const id = dirent.name;
    const files = (await readdir(join(PUBLIC_PLACES, id)))
      .filter((f) => IMAGE_EXT.test(f))
      .sort();

    if (files.length === 0) continue;

    const gallery = files.map((file, index) => {
      const source = file.startsWith("gallery-") ? "google" : "official";
      return {
        src: `/places/${id}/${file}`,
        alt: `${id} photo ${index + 1}`,
        source,
      };
    });

    // Extract videos/menuUrl from previous generated text if present
    const blockRe = new RegExp(
      `"${id}"\\s*:\\s*(\\{[\\s\\S]*?\\n  \\}),`,
    );
    const blockMatch = prevRaw.match(blockRe);
    let videos = [];
    let menuUrl;
    let googlePlaceId;
    let phone;
    let geo;
    if (blockMatch) {
      try {
        const obj = Function(`"use strict"; return (${blockMatch[1]})`)();
        videos = obj.videos ?? [];
        const candidateMenu = obj.menuUrl;
        if (
          candidateMenu &&
          /menu/i.test(candidateMenu) &&
          !/\.(css|js)(\?|$)/i.test(candidateMenu) &&
          !/parastorage|wixui/i.test(candidateMenu)
        ) {
          menuUrl = candidateMenu;
        }
        googlePlaceId = obj.googlePlaceId;
        phone = obj.phone;
        geo = obj.geo;
      } catch {
        /* keep empty */
      }
    }

    // Prefer curated menu URLs from place-menus via enrichment; only keep clean generated ones
    mediaById[id] = {
      image: gallery[0].src,
      gallery: gallery.map((item, index) => ({
        ...item,
        alt: `${id.replace(/-/g, " ")} photo ${index + 1}`,
      })),
      ...(videos.length ? { videos } : {}),
      ...(menuUrl ? { menuUrl } : {}),
      ...(googlePlaceId ? { googlePlaceId } : {}),
      ...(phone ? { phone } : {}),
      ...(geo ? { geo } : {}),
    };

    console.log(`${id}: ${gallery.length} images`);
  }

  const entries = Object.entries(mediaById)
    .map(
      ([id, bundle]) =>
        `  ${JSON.stringify(id)}: ${JSON.stringify(bundle, null, 4).replace(/\n/g, "\n  ")},`,
    )
    .join("\n");

  const out = `/**
 * Generated by \`npm run places:media\` / rebuild-place-media-from-disk.
 * Do not edit by hand — re-run the script to refresh.
 */
import type {
  PlaceGalleryImage,
  PlaceMenuSection,
  PlaceVideo,
} from "./place-types";

export type PlaceMediaBundle = {
  image?: string;
  gallery?: readonly PlaceGalleryImage[];
  menu?: readonly PlaceMenuSection[];
  menuUrl?: string;
  videos?: readonly PlaceVideo[];
  googlePlaceId?: string;
  phone?: string;
  geo?: { lat: number; lng: number };
};

export const placeMediaById: Record<string, PlaceMediaBundle> = {
${entries}
};
`;

  await writeFile(OUT_FILE, out, "utf8");
  console.log(`Wrote ${OUT_FILE}`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
